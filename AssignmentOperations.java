public class AssignmentOperations {
    public static void main(String[] args) {
        int a = 5;
        System.out.println(a);
        a += 10;
        System.out.println(a);
        a -= 3;
        System.out.println(a);
        a *= 2;
        System.out.println(a);
        a /= 6;
        System.out.println(a);
        a <<= 4;
        System.out.println(a);
        a >>= 2;
        System.out.println(a);
    }
}
